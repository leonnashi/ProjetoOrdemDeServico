    private void jRadioButtonClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonClienteActionPerformed
        if (jRadioButtonCliente.isSelected()) {
            try {
                Cliente cliente = new Cliente();
                NegocioCliente negocioCliente = new NegocioCliente();
                respostaCliente = negocioCliente.listar(cliente);
                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(new Object[]{"Nome", "CPF", "RG"});

                for (Cliente c : this.respostaCliente) {
                    modelo.addRow(new Object[]{c.getNome(), c.getCpf(), c.getRg()});
                }

                jTable1.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }

    }//GEN-LAST:event_jRadioButtonClienteActionPerformed

    private void jRadioButtonFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonFuncionarioActionPerformed
        if (jRadioButtonFuncionario.isSelected()) {
            try {
                Funcionario funcionario = new Funcionario();
                NegocioFuncionario negocioFuncionario = new NegocioFuncionario();
                respostaFuncionario = negocioFuncionario.listar(funcionario);
                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(new Object[]{"Nome", "CPF", "RG", "Salario"});

                for (Funcionario f : this.respostaFuncionario) {
                    modelo.addRow(new Object[]{f.getNome(), f.getCpf(), f.getRg(), f.getSalario()});
                }

                jTable1.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jRadioButtonFuncionarioActionPerformed

    private void jRadioButtonClassProblemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonClassProblemaActionPerformed
        if (jRadioButtonClassProblema.isSelected()) {
            try {
                ClassificacaoProblema classificacaoProblema = new ClassificacaoProblema();
                NegocioClassificacao negocioClassificacao = new NegocioClassificacao();
                respostaClassificacao = negocioClassificacao.listar(classificacaoProblema);
                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(new Object[]{"Código", "Nome", "Descricao"});

                for (ClassificacaoProblema cp : this.respostaClassificacao) {
                    modelo.addRow(new Object[]{cp.getCodigo(), cp.getNome(), cp.getDescricao()});
                }

                jTable1.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jRadioButtonClassProblemaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        MouseEvent e = evt;
        int click = e.getClickCount();
        int index = jTable1.getSelectedRow();

        if (jRadioButtonCliente.isSelected()) {
            Cliente cliente = this.respostaCliente.get(index);
            jTextFieldCPFCliente.setText(cliente.getCpf());
        }
        if (jRadioButtonFuncionario.isSelected()) {
            Funcionario funcionario = this.respostaFuncionario.get(index);
            jTextFieldMatriculaFun.setText(String.valueOf(funcionario.getMatricula()));
        }
        if (jRadioButtonClassProblema.isSelected()) {
            ClassificacaoProblema classProblema = this.respostaClassificacao.get(index);
            jTextFieldCodClass.setText(String.valueOf(classProblema.getCodigo()));
        }
    }//GEN-LAST:event_jTable1MouseClicked


    private void jButtonCadastrarDescricaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarDescricaoActionPerformed

        try {

            OrdemServico ordemServico = new OrdemServico();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            jTextFieldProcurarData.setText(formato.format(new Date(System.currentTimeMillis())));
            Date dataCadastro = new java.sql.Date(formato.parse(jTextFieldProcurarData.getText()).getTime());
            ordemServico.setDataCadastro(dataCadastro);
            ordemServico.getClassificacao().setCodigo(Integer.parseInt(jTextFieldCodClass.getText()));
            ordemServico.getCliente().setCpf(jTextFieldCPFCliente.getText());
            ordemServico.getFuncionario().setMatricula(Integer.parseInt(jTextFieldMatriculaFun.getText()));
            ordemServico.setDescricaoProblema(jTextAreaDescricao.getText());
            NegocioOrdemServico negocioOrdem = new NegocioOrdemServico();
            negocioOrdem.cadastrar(ordemServico);
            JOptionPane.showMessageDialog(null, "Ordem de Servico cadastrada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Falta selecinar algum campo (CPF, Matricula, Classificação)");
        }
    }//GEN-LAST:event_jButtonCadastrarDescricaoActionPerformed


    private void jButtonSalvarClassificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarClassificacaoActionPerformed
        if (jTableListarClassificacao.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Você precisa selecionar um campo na tabela");
            return;
        }

        int index = jTableListarClassificacao.getSelectedRow();
        ClassificacaoProblema classProblema = this.respostaClassificacao.get(index);
        ClassificacaoProblema classificacao = new ClassificacaoProblema();
        classificacao.setNome(jTextFieldNomeClassificacao.getText());
        classificacao.setDescricao(jTextAreaDescricao.getText());
        classificacao.setCodigo(classProblema.getCodigo());
        NegocioClassificacao negocioClassificacao = new NegocioClassificacao();
        try {
            negocioClassificacao.atualizar(classificacao);
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            ListarClassificacao();
            jTextFieldNomeClassificacao.setText("");
            jTextAreaDescricao.setText("");
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarClassificacaoActionPerformed


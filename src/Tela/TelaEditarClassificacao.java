    public void ListarClassificacao() {
        try {
            ClassificacaoProblema classificacaoProblema = new ClassificacaoProblema();
            NegocioClassificacao negocioClassificacao = new NegocioClassificacao();
            respostaClassificacao = negocioClassificacao.listar(classificacaoProblema);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Nome", "Descricao"});

            for (ClassificacaoProblema cl : this.respostaClassificacao) {
                modelo.addRow(new Object[]{cl.getNome(), cl.getDescricao()});
            }
            jTableListarClassificacao.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }


    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed

            try {
                ClassificacaoProblema classificacao = new ClassificacaoProblema();
                classificacao.setCodigo(Integer.parseInt(jTextFieldCodigo.getText()));

                NegocioClassificacao negocioClassificacao = new NegocioClassificacao();
                negocioClassificacao.remover(classificacao);
                JOptionPane.showMessageDialog(null, "O problema foi removido!");
                this.dispose();
                new TelaEditarClassificacao().setVisible(true);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Os dados não coincidem com os dados da tabela! Por favor, selecionar novamente");
                this.dispose();
                new TelaEditarClassificacao().setVisible(true);
            }
    }//GEN-LAST:event_jButtonRemoverActionPerformed


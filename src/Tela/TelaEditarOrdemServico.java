    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        try {
            OrdemServico ordemServico = new OrdemServico();
            ordemServico.setNumero(Integer.parseInt(jTextFieldNumeroOrdem.getText()));

            NegocioOrdemServico negocioOrdemServico = new NegocioOrdemServico();
            negocioOrdemServico.remover(ordemServico);
            JOptionPane.showMessageDialog(null, "A ordem de serviço foi removida!");
            this.dispose();
            new TelaEditarOrdemServico().setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Falta selecinar algum campo (CPF, Matricula, Classificação)");
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed


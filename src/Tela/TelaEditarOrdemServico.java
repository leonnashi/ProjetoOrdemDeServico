    private void jRadioButtonOrdemServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonOrdemServicoActionPerformed
        if (jRadioButtonOrdemServico.isSelected() == true) {
            jButtonRemover.setVisible(true);
            try {
                OrdemServico ordemServico = new OrdemServico();
                NegocioOrdemServico negocioOrdemServico = new NegocioOrdemServico();
                respostaServico = negocioOrdemServico.listar(ordemServico);
                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(new Object[]{"Cliente", "CPF do Cliente", "Funcionario", "Data de Cadastro", "Problema"});

                for (OrdemServico os : this.respostaServico) {
                    modelo.addRow(new Object[]{os.getCliente().getNome(), os.getCliente().getCpf(), os.getFuncionario().getNome(), os.getDataCadastro(), os.getClassificacao().getNome()});
                }

                jTableOrdemServico.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Falta selecinar algum campo (CPF, Matricula, Classificação)");
            }
        }
    }//GEN-LAST:event_jRadioButtonOrdemServicoActionPerformed


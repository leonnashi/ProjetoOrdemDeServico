    private void jRadioButtonClassProblemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonClassProblemaActionPerformed
        if (jRadioButtonClassProblema.isSelected()) {
            jButtonRemover.setVisible(false);
            try {
                ClassificacaoProblema classificacaoProblema = new ClassificacaoProblema();
                NegocioClassificacao negocioClassificacao = new NegocioClassificacao();
                respostaClassificacao = negocioClassificacao.listar(classificacaoProblema);
                DefaultTableModel modelo = new DefaultTableModel();
                modelo.setColumnIdentifiers(new Object[]{"Código", "Nome", "Descricao"});

                for (ClassificacaoProblema cp : this.respostaClassificacao) {
                    modelo.addRow(new Object[]{cp.getCodigo(), cp.getNome(), cp.getDescricao()});
                }

                jTableOrdemServico.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Falta selecinar algum campo (CPF, Matricula, Classificação)");
            }
        }
    }//GEN-LAST:event_jRadioButtonClassProblemaActionPerformed


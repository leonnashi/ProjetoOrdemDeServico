    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        try {

            OrdemServico ordemServico = new OrdemServico();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            jTextFieldProcurarData.setText(formato.format(new Date(System.currentTimeMillis())));
            Date dataCadastro = new java.sql.Date(formato.parse(jTextFieldProcurarData.getText()).getTime());
            ordemServico.setDataCadastro(dataCadastro);
            ordemServico.getClassificacao().setCodigo(Integer.parseInt(jTextFieldCodClass.getText()));
            ordemServico.getCliente().setCpf(jTextFieldCPFCliente.getText());
            ordemServico.getFuncionario().setMatricula(Integer.parseInt(jTextFieldMatriculaFun.getText()));
            ordemServico.setDescricaoProblema(jTextAreaDescricao.getText());
            ordemServico.setNumero(Integer.parseInt(jTextFieldNumeroOrdem.getText()));
            NegocioOrdemServico negocioOrdem = new NegocioOrdemServico();
            negocioOrdem.atualizar(ordemServico);
            JOptionPane.showMessageDialog(null, "Ordem de Servico cadastrada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "A Ordem de serviço não esta cadastrada! Tenta selecionar outra Ordem de Serviço previamente cadastrada");
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jTableOrdemServicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableOrdemServicoMouseClicked
        MouseEvent e = evt;
        int click = e.getClickCount();
        int index = jTableOrdemServico.getSelectedRow();

        if (jRadioButtonOrdemServico.isSelected() == true) {
            OrdemServico ordem = this.respostaServico.get(index);
            jTextFieldNumeroOrdem.setText(String.valueOf(ordem.getNumero()));
        }
        if (jRadioButtonCliente.isSelected()) {
            Cliente cliente = this.respostaCliente.get(index);
            jTextFieldCPFCliente.setText(cliente.getCpf());
        }
        if (jRadioButtonFuncionario.isSelected()) {
            Funcionario funcionario = this.respostaFuncionario.get(index);
            jTextFieldMatriculaFun.setText(String.valueOf(funcionario.getMatricula()));
        }
        if (jRadioButtonClassProblema.isSelected()) {
            ClassificacaoProblema classProblema = this.respostaClassificacao.get(index);
            jTextFieldCodClass.setText(String.valueOf(classProblema.getCodigo()));
        }
    }//GEN-LAST:event_jTableOrdemServicoMouseClicked


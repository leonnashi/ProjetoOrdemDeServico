    private void jButtonPesquisarCPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarCPFActionPerformed
        try {
            Cliente clienteNovo = new Cliente();
            clienteNovo.setCpf(jTextFieldProcurarCPF.getText());
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Nome", "Cpf", "Rg"});
            NegocioCliente negocioCliente = new NegocioCliente();
            this.respostaCliente = negocioCliente.listar(clienteNovo);

            for (Cliente c : this.respostaCliente) {
                modelo.addRow(new Object[]{c.getNome(), c.getCpf(), c.getRg()});
            }
            jTableOrdemServico.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonPesquisarCPFActionPerformed

